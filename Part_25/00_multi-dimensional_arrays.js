// Multi-dimensional arrays: Arrays inside array | Array of Arrays

const numbers = [1, 2, 3, 4];
console.log("numbers", numbers);
console.log("numbers[0]", numbers[0]);
console.log("numbers[1]", numbers[1]);

const twoDArray = [
  [1, 2, 3],
  [4, 5, 6],
];
console.log("twoDArray", twoDArray);
console.log("twoDArray[0][0]", twoDArray[0][0]);
console.log("twoDArray[1][1]", twoDArray[1][1]);
